#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	tmp;

	while ((tmp = *s) != (char)c)
	{
		if (tmp == '\0')
			return ((char*)0);
		s++;
	}
	return ((char*)s);
}
