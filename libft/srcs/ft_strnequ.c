#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (n == 0)
		return (1);
	while (n-- > 1 && *s1 != '\0' && *s1 == *s2)
	{
		if (n == 0)
			return (1);
		s1++;
		s2++;
	}
	return (!(*s1 - *s2));
}
